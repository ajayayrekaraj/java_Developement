package methods_OR_functions;

public class funtion_practise {
	public static void main(String []args) {
		System.out.println("Hey");
		int a=9,b=7;
		int c=a+b;
		Add();
		System.out.println(c);
	}
	public static void Add() {
		int a=11,b=17;
		sub();
		int c=a+b;
		System.out.println(c);
	}
	public static void sub() {
		int a=11,b=17;
		int c=a-b;
		System.out.println(c);
	}
}
