package JavaCodesDay1;

public class comparison {
	public static void main(String[]args) {
		int x=10,y=20;
		if(x==y)
			System.out.println(true);
		else
			System.out.println(false);
		
		if(x!=y)
			System.out.println(true);
		else
			System.out.println(false);
		
		if(x>y)
			System.out.println(false);
		else
			System.out.println(true);
		
		if(x>=y)
			System.out.println(false);
		else
			System.out.println(true);
		
		
		if(x<y)
			System.out.println(true);
		else 
			System.out.println(false);
		
		if(x<=y)
			System.out.println(true);
		else
			System.out.println(false);
	}

}
